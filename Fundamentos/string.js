const escola = 'cod3r'
// charAt => vai dar a letra que está na posição 4
console.log(escola.charAt(4))
// charCodeAt => pega o valor dela na tabela ascii
console.log(escola.charCodeAt(3))
// indexOf => em que posição o digito  está na palavra
console.log(escola.indexOf('3'))
// substring => retorna a parte da palavra que começa a partir do index
console.log(escola.substring(0, 3))
//replace => substitui a letra que está na posição 3 por outra
console.log(escola.replace(3, 'e'))
//split => divide a palavra por espaço e retorna uma lista
console.log('Ana, maria'.split(' '))